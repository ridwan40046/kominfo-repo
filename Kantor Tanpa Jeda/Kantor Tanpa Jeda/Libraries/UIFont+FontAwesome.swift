import Foundation
import UIKit

extension UIFont {
    
    static func fontAwesome (size: CGFloat) -> UIFont {
        return UIFont(name: "FontAwesome", size: size)!
    }
    
    static func lineAwesome (size: CGFloat) -> UIFont {
        return UIFont(name: "LineAwesome", size: size)!
    }
    
}
